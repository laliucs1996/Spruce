    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String p1 = request.getParameter("p1");
        String p2 = request.getParameter("p2");
        HttpSession session = request.getSession();
        String name2  = (String) session.getAttribute("email");
        User user = uf.getUser(name2);
        String hashedpw = BCrypt.hashpw(p2, BCrypt.gensalt());
        if(BCrypt.checkpw(p1, user.getPassWord())) 
        {
            user.setPassWord(hashedpw);
            uf.updateUser(user);
            request.setAttribute("sucesssP", true);
        }
        else
        {
            request.setAttribute("sucesssP", false);
        }
        RequestDispatcher dispatcher = getServletContext().getRequestDispatcher("/DisplayUserServlet");
        dispatcher.forward(request,response);
    }

